import turtle
my_turtle = turtle.Turtle()

# square function
def square():                           # defining a function
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)

square()                                # calling a function

